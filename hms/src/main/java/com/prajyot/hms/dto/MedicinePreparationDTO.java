package com.prajyot.hms.dto;

public class MedicinePreparationDTO {

    private Integer prepId;
    private Integer medicineId;  // To reference the Medicine entity
    private Integer doses;
    private String status;

    // Getters and Setters

    public Integer getPrepId() {
        return prepId;
    }

    public void setPrepId(Integer prepId) {
        this.prepId = prepId;
    }

    public Integer getMedicineId() {
        return medicineId;
    }

    public void setMedicineId(Integer medicineId) {
        this.medicineId = medicineId;
    }

    public Integer getDoses() {
        return doses;
    }

    public void setDoses(Integer doses) {
        this.doses = doses;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
